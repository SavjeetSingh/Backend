import mongoose, { Mongoose } from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
      match: [/^\S+@\S+\.\S+$/, "Please use a valid email address"],
    },
    password: {
      type: String,
      required: true,
    },
    timezone: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

export default mongoose.model("User", UserSchema);
